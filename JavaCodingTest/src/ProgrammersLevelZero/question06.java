package ProgrammersLevelZero;

import java.util.Scanner;

public class question06 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        //int형 두개를 입력받아 변수에 저장한다.

        System.out.println(a+" + "+b+" = "+(a+b));
        // a + b = a+b 꼴로 출력해준다.
    }
}
