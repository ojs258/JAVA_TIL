package programmersLevelZero;

import java.util.Scanner;

public class question01 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in); //문자열을 입력받기 위한 Scanner 객체 호출
        String input = sc.next(); //문자열을 입력받아 변수 input에 저장
        System.out.println(input); // 입력받은 문자열을 출력
    }
}
